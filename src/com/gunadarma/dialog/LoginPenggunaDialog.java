    private void btnSignInActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSignInActionPerformed
        // proses login
       
            String username = txtUsername.getText();
            char[] pwd = txtPassword.getPassword();
            String password = new String(pwd);
            
            if(validasi() == true){
                Pengguna loginPengguna = penggunaDao.loginPengguna(username, password);
                if(loginPengguna != null){
                    p = loginPengguna;
                    Master tampilkanMaster = new Master();
                    tampilkanMaster.setVisible(true);
                    dispose();
                }else{
                    JOptionPane.showMessageDialog(null, "Username dan password tidak valid !");
                }
            }
    }//GEN-LAST:event_btnSignInActionPerformed

